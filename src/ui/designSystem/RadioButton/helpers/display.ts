import { RadioDisplayOptions } from 'ui/designSystem/RadioButton/types'
import { SelectableDisplay } from 'ui/designSystem/types'

export function getRadioDisplay(input: RadioDisplayOptions): SelectableDisplay {
  if (input.variant === 'detailed' && input.collapsed) return 'fill'
  if (input.variant === 'detailed') return input.display ?? 'hug'
  return 'hug'
}

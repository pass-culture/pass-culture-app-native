export const getAfterTextParts = ({
  keepFirstWordWithLink,
  text,
}: {
  keepFirstWordWithLink: boolean
  text: string
}) => {
  if (keepFirstWordWithLink) return splitFirstWord(text)

  return {
    firstWord: undefined,
    trailingText: text.trim(),
  }
}

const splitFirstWord = (text: string) => {
  const trimmedText = text.trim()
  const firstSpaceIndex = trimmedText.indexOf(' ')

  if (firstSpaceIndex === -1) return { firstWord: trimmedText, trailingText: undefined }

  return {
    firstWord: trimmedText.slice(0, firstSpaceIndex),
    trailingText: trimmedText.slice(firstSpaceIndex + 1),
  }
}

import React from 'react'
import type { TouchableOpacityProps } from 'react-native'

import { ColorsType } from 'theme/types'
import { AccessibleIcon } from 'ui/svg/icons/types'

export type LinkSize = 'default' | 'small' | 'extraSmall'
export type LinkColor = 'brand' | 'neutral'

export type LinkBaseProps = {
  label?: string
  wording?: string
  size?: LinkSize
  color?: LinkColor
  icon?: React.FunctionComponent<AccessibleIcon>
  isExternal?: boolean
  isInsideText?: boolean
  textColor?: ColorsType
  testID?: string
}

export type LinkProps = LinkBaseProps & Omit<TouchableOpacityProps, 'children'>

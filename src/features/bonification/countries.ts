export interface Country {
  /** Unique numeric identifier. */
  id: number

  /** String representation of the country's flag (e.g., emoji). */
  flag: string

  /** Two-letter country code (ISO 3166-1 alpha-2). */
  alpha2: string

  /** Three-letter country code (ISO 3166-1 alpha-3). */
  alpha3: string

  /** Top-level domain for the country. */
  tld: string

  /** The common name of the country. */
  name: string

  /** The name of the country in French. */
  name_fr: string

  /** The name of the country in English. */
  name_en: string

  /** The geographical region in French. */
  region_fr: string

  /** The geographical region in English. */
  region_en: string

  /** The geographical sub-region in French. */
  sub_region_fr: string

  /** The geographical sub-region in English. */
  sub_region_en: string

  /** The native name of the primary language. */
  lang: string

  /** The name of the primary language in French. */
  lang_fr: string

  /** The name of the primary language in English. */
  lang_en: string

  /** The ISO 639-1 code for the language. */
  lang_iso: string

  /** The locale code for the language. */
  lang_local: string | null

  /** The writing direction of the language ('Left-To-Right' or 'Right-To-Left'). */
  lang_direction: 'LTR' | 'RTL'

  /** The international dialing code. */
  indicatif: string | null

  /** The typical length of a phone number in the country. */
  phone_length: number | null

  /** The name of the official currency. */
  monetary: string | null

  /** The ISO 4217 code for the currency. */
  iso_4217: string | null

  /** The symbol for the currency. */
  symbol: string | null
}

